    private void SeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarActionPerformed
        // TODO add your handling code here:
        int fila = InformacionEmpleados.getSelectedRow();
    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor selecciona una fila.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) InformacionEmpleados.getModel();

    DNITF.setText(model.getValueAt(fila, 0).toString());
    NombreTF.setText(model.getValueAt(fila, 1).toString());
    CelularTF.setText(model.getValueAt(fila, 2).toString());
    CorreoTF.setText(model.getValueAt(fila, 3).toString());
    NumeroDeCuenta.setText(model.getValueAt(fila, 4).toString());
    BancoTF.setText(model.getValueAt(fila, 5).toString());
    ContraseñaTF.setText(model.getValueAt(fila, 6).toString());
    CargoTF.setText(model.getValueAt(fila, 7).toString());

    DNITF.setEditable(false); // No se debe cambiar el DNI porque es clave primaria
    }//GEN-LAST:event_SeleccionarActionPerformed

    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        
        
        String dni = DNITF.getText().trim();
        String nombre = NombreTF.getText().trim();
        String celular = CelularTF.getText().trim();
        String correo = CorreoTF.getText().trim();
        String numeroCuenta = NumeroDeCuenta.getText().trim();
        String banco = BancoTF.getText().trim();
        String contrasena = ContraseñaTF.getText().trim();
        String cargo = CargoTF.getText().trim();

    // 1️⃣ Validar que no haya campos vacíos
    if (dni.isEmpty() || nombre.isEmpty() || celular.isEmpty() || correo.isEmpty() ||
        numeroCuenta.isEmpty() || banco.isEmpty() || contrasena.isEmpty() || cargo.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor completa todos los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 2️⃣ Validaciones específicas
    if (!dni.matches("\\d{8}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El DNI debe tener exactamente 8 dígitos numéricos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!nombre.matches("^[A-Za-zÁÉÍÓÚáéíóúñÑ ]+$")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El nombre solo puede contener letras y espacios.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!celular.matches("\\d{9}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El número de celular debe tener exactamente 9 dígitos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!correo.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El correo electrónico no tiene un formato válido.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!numeroCuenta.matches("\\d{20}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "El número de cuenta debe tener exactamente 20 dígitos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!contrasena.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&#%'?¡¿])[A-Za-z\\d@$!%*?&]{8,}$")) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "La contraseña debe tener al menos 8 caracteres, incluir una mayúscula, una minúscula, un número y un carácter especial.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 3️⃣ Insertar en base de datos
    String sql = "INSERT INTO empleados (dni, nombres_apellidos, celular, correo, numero_cuenta, banco, contrasena, cargo) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

    try (java.sql.Connection con = Conexion.conectar();
         java.sql.PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setString(1, dni);
        ps.setString(2, nombre);
        ps.setString(3, celular);
        ps.setString(4, correo);
        ps.setString(5, numeroCuenta);
        ps.setString(6, banco);
        ps.setString(7, contrasena);
        ps.setString(8, cargo);

        ps.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Empleado agregado correctamente.");

        // 4️⃣ Agregar fila a la tabla
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) InformacionEmpleados.getModel();
        model.addRow(new Object[]{dni, nombre, celular, correo, numeroCuenta, banco, contrasena, cargo});

        // 5️⃣ Limpiar campos
        DNITF.setText("");
        NombreTF.setText("");
        CelularTF.setText("");
        CorreoTF.setText("");
        NumeroDeCuenta.setText("");
        BancoTF.setText("");
        ContraseñaTF.setText("");
        CargoTF.setText("");

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar empleado: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_AgregarActionPerformed

