    private void cargarEmpleadosDesdeBD() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) InformacionEmpleados.getModel();
    model.setRowCount(0); // Limpia la tabla

    String sql = "SELECT dni, nombres_apellidos, celular, correo, numero_cuenta, banco, contrasena, cargo FROM empleados";

    try (java.sql.Connection con = Conexion.conectar();
         java.sql.Statement st = con.createStatement();
         java.sql.ResultSet rs = st.executeQuery(sql)) {

        while (rs.next()) {
            String dni = rs.getString("dni");
            String nombre = rs.getString("nombres_apellidos");
            String celular = rs.getString("celular");
            String correo = rs.getString("correo");
            String numeroCuenta = rs.getString("numero_cuenta");
            String banco = rs.getString("banco");
            String contrasena = rs.getString("contrasena");
            String cargo = rs.getString("cargo");

            model.addRow(new Object[]{dni, nombre, celular, correo, numeroCuenta, banco, contrasena, cargo});
        }

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar los empleados: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}

