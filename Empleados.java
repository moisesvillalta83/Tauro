    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = InformacionEmpleados.getSelectedRow();

    if (filaSeleccionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un empleado de la tabla para eliminar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    String dni = InformacionEmpleados.getValueAt(filaSeleccionada, 0).toString();

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Estás seguro de eliminar al empleado con DNI: " + dni + "?", 
            "Confirmar eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

    // SQL para eliminar el empleado
    String sql = "DELETE FROM empleados WHERE dni = ?";

    try (java.sql.Connection con = Conexion.conectar();
         java.sql.PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setString(1, dni);
        int filasEliminadas = ps.executeUpdate();

        if (filasEliminadas > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");

            // Eliminar la fila de la tabla
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) InformacionEmpleados.getModel();
            model.removeRow(filaSeleccionada);

            // Limpiar campos
            DNITF.setText("");
            NombreTF.setText("");
            CelularTF.setText("");
            CorreoTF.setText("");
            NumeroDeCuenta.setText("");
            BancoTF.setText("");
            ContraseñaTF.setText("");
            CargoTF.setText("");

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontró el empleado para eliminar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar empleado: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_EliminarActionPerformed

